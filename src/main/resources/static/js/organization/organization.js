$(document).ready(function() {
	koreaOrgaArr.forEach(orga => {
		divisionKeys.forEach(division => {
			if(orga[division].name){
					var chp_boxer_data = `<img width="50px" height="50px" src="/img/boxerProfileImg/${orga[division].id}.jpg" onerror="if (this.src != 'error.jpg') this.src = '/img/boxerProfileImg/defaultImage.png';" alt="..."  >`
							+ `<label style="font-weight:bold;">${orga[division].name}</label>`;
				
				if(orga[division].id){
					$.ajax({
			            type: "get",
			            url: "/boxer/boxerdetail",
			            data: "boxerId=" + orga[division].id,
			            dataType: "json",
			            success: function(result) {
							chp_boxer_data = chp_boxer_data +
								`<label style="font-weight:bold;">`
							+ 		`<span style="color:blue;">${result.result.win}</span> - <span style="color:red;">${result.result.lose}</span> - <span style="color:green;">${result.result.draw}</span>` 
							+	`</label>`;
							
							$(`#chp_tr_${orga['orga_name']}`).find(`.chp_${division}`).append(chp_boxer_data);
							$(`#chp_tr_${orga['orga_name']}`).find(`.chp_${division}`).on('click', () => openBoxerDetailModal(orga[division].id));
						}
					});
				}else{
					$(`#chp_tr_${orga['orga_name']}`).find(`.chp_${division}`).append(chp_boxer_data);
				}	
			}
		})
	})
	
	showHideOrgaChap();
});


function showHideOrgaChap(){
	$(".organization_champ_body").find(".orgaChkbox").each((index,item)=>{
		var displayAttrVal = item.checked? "table-row":"none";
		$(`#chp_tr_${item.value}`).css("display",displayAttrVal);
	});
}

